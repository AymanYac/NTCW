package model;

public class AbaqueRow {
	Integer bs;
	double class_accuracy;
	Integer tc;
	double ta;
	double coverage;
	double family_accuracy;
	public Integer getBs() {
		return bs;
	}
	public void setBs(Integer bs) {
		this.bs = bs;
	}
	public double getClass_accuracy() {
		return class_accuracy;
	}
	public void setClass_accuracy(double class_accuracy) {
		this.class_accuracy = class_accuracy;
	}
	public Integer getTc() {
		return tc;
	}
	public void setTc(Integer tc) {
		this.tc = tc;
	}
	public double getTa() {
		return ta;
	}
	public void setTa(double ta) {
		this.ta = ta;
	}
	public double getCoverage() {
		return coverage;
	}
	public void setCoverage(double coverage) {
		this.coverage = coverage;
	}
	public double getFamily_accuracy() {
		return family_accuracy;
	}
	public void setFamily_accuracy(double family_accuracy) {
		this.family_accuracy = family_accuracy;
	}
	
	
	
}
