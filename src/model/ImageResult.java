package model;

public class ImageResult {
	
	String url;
	long height;
	long width;
	
	
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public long getHeight() {
		return height;
	}
	public void setHeight(long image_height) {
		this.height = image_height;
	}
	public long getWidth() {
		return width;
	}
	public void setWidth(long image_width) {
		this.width = image_width;
	}
	
	
	
}
