package model;

public class CharClassProposition {

	String segment_id;
	String segment_name;
	String proposer;
	Boolean expectMore;
	
	
	public String getSegment_id() {
		return segment_id;
	}
	public void setSegment_id(String segment_id) {
		this.segment_id = segment_id;
	}
	public String getSegment_name() {
		return segment_name;
	}
	public void setSegment_name(String segment_name) {
		this.segment_name = segment_name;
	}
	public String getProposer() {
		return proposer;
	}
	public void setProposer(String proposer) {
		this.proposer = proposer;
	}
	public Boolean getExpectMore() {
		return expectMore;
	}
	public void setExpectMore(Boolean expectMore) {
		this.expectMore = expectMore;
	}
	
	
}
